public class StringExercise {
    public static void main (String[] args) {
        String message = "We don't need no education\n" +
                "We don't need no thought control\n" +
                "Check \"this\" out!, \"s inside of \"s! \n" +
                "In windows, the main drive is usually C:\\ \n" +
                "I can do backslashes \\, double backslash \\\\, \n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(message);
        System.out.println(" ");
        System.out.println(" ");

        String message1 = "We don't need no education";
        String message2 = "We don't need no thought control";
        String message3 = "Check \"this\" out!, \"s inside of \"s!";
        String message4 = "In windows, the main drive is usually C:\\";
        String message5 = "I can do backslashes \\, double backslash \\\\,";
        String message6 = "and the amazing triple backslash \\\\\\!";
        System.out.println(message1);
        System.out.println(message2);
        System.out.println(message3);
        System.out.println(message4);
        System.out.println(message5);
        System.out.println(message6);
    }
}
